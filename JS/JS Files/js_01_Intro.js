// Arithmetic operators
var num1 = 57;
var num2 = 66;
console.log("The value of num1 + num2 is: "+(num1+num2));
console.log("The value of num1 - num2 is: "+(num1-num2));
console.log("The value of num1 * num2 is: "+(num1*num2));
console.log("The value of num1 / num2 is: "+(num1/num2));
console.log("The value of num1 ** num2 is: "+(num1**num2));
console.log("The value of num1++ is: "+(num1++));
console.log("The value of ++num1 is: "+(++num1));
console.log("The value of num1-- is: "+(num1--));
console.log("The value of --num1 is: "+(--num1));